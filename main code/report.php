<h1>Evaluation</h1>
<p>
One of the pioneers who tried to evaluate the user experience on digital platforms was Jakob Nielsen with his heuristic evaluation. <br>Even though it is old, it still is relevant today of technology and user usability. In this report, I am going to explain some of these rules regarding my website as an example.<br> Visibility of system status is important because the system must communicate with the user to tell them the current system state.<br> What I mean by this is the user must know why certain things are there. For example, in my website, on the landing page the user sees a product link which takes the user to the products available on the website.<br> The user also sees four products that are the showcase on the website. The user could think why are those ones selected, is it on sale?<br> Are they new? Are they exclusive? If you do not state what is going on, the user will not know. <br><br>
Therefore, on my landing page, it says ‘Recently Added Products’, so any new products added to the website’s database it will refresh on the page. This brings clarity to the user to know that these are new products added to the website. <br>The second one is the match between system and real world. The design should speak the users’ language.  By that, I mean words, phrases, and concepts familiar to the user.<br> It is not only words, but images should also have a corelation to the word if you are trying to create a relationship between them. For example, on my website for the shopping cart link, it is an icon of a shopping basket. <br>Without me writing the actual link, the user already knows where the click will take them to. It is also important for the website to speak the language of the user. For example, if a user wanted to log-in, but the user realises that they do not have an account, instead of the website just saying, ‘Sign Up’, it would say ‘You don’t have an account? Log in here?’ It sets the context and speaks the everyday language.<br><br> User Control and Freedom. This principle involved about giving the user freedom to navigate and perform actions and to undo them as well. This can be illustrated on my website when the user mistakenly adds an item to cart. When it takes the user to the shopping basket, the user could remove the item and it undo the previous action.
	Consistent and standards is one of the most principles because consistency is the key to a good website design. On my website, on public pages, the headers are the same no matter where the user navigates to, and it is vice versa to users that have logged in. The colour scheme is the same throughout the website to create a brand identity. The logo is always located at the same spot wherever the user navigates too as well. Failing to maintain consistency may increase the users’ cognitive load by forcing them to learn something new. Regarding error prevention, the best designs prevent problems from occurring in the first place, for example in when the user logs in on the website, while the user attempts to input their details, I implemented a placeholder, so the user can see what should be inputted in the form box, so this eliminates the user making a mistake because they know what to input. My website also shows principles of flexibility and efficiency, this principle means it may speed up the interaction for the user. An example of this on my website the user can add multiple products to the shopping basket, instead of buying it individually and it is efficient because the website puts all products selected in the basket and it calculates the total price for the total amount. 
	Aesthetic and minimalist design was a principle I focused on implementing while I was designing my website. Interfaces should not contain information which is irrelevant or rarely needed. I had to make sure that if every piece of information displayed on interface is necessary and useful. The visual element of the interface must support the user’s primary goals and that is to have an easy going and clutter-free experience that gives good usability.  The website helps users to recognize, diagnose, and recover from errors. Errors are inadvertent in the user journey. A check needs to be made if those errors are being explained to the user in understandable language. For example, if the user enters the wrong username or password, the error message I got is either the username or the password is incorrect.

 </p><br><br>
 <H1> Comparison </H1>

 <p>  I am comparing my final design to ASOS because they are both clothing websites. They both have similar styles because they both make use of the white space and the design offers a clutter-free experience for both users. However, the ASOS website makes use of different bright colours and a lot more images which can be seen as attention-grabbing to the user. Both websites use short amount of text and sentences and both use good amount of eye-catching language to engage the user which is important because it keeps the user interested. For example, in my website, when the user looks at the product description, it looks convincing for the user to buy the product. The different is on the ASOS website, there is many examples of that across the website, with the use of humour or clever wordplay and the use of slang for certain phrases, it tries to relate to the user, and it creates a connection to the user to the brand. 
The ASOS website also implements the use of different social media buttons and comments and replies, like reviews within a site. To improve my website, I would have implemented that because the more information you have, the better you can apply your knowledge to your own website. Some of the tools listed above will give you social media statistics, including number of shares, likes, follows, comments and so on.  The greater interaction and engagement a site is providing, the greater it will rank in search engine results. Both use different typographical styles depending on what the user is viewing, and both use horizontal navigation menus. However, the ASOS website makes use of a drop-down system which shows the user more options to find what they want, so it makes it flexible because it leads the user to a category of one type of product. It also implements a search bar, so the user can search for one product directly. These are things I would use if I were to improve my website further. 



